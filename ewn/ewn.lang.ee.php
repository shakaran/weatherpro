function charset($word) {
$word = str_replace('æ', '&aelig;', $word);
$word = str_replace('ø ', '&oslash;', $word);
$word = str_replace('å ', '&aring;', $word);
$word = str_replace('ä', '&auml;', $word);
$word = str_replace('ö', '&ouml;', $word);
$word = str_replace('Å ', '&Aring;', $word);
$word = str_replace('Ä', '&Auml;', $word);
$word = str_replace('Ö', '&Ouml;', $word);
$word = str_replace('Ø ', '&Oslash;', $word);

return $word;
}

